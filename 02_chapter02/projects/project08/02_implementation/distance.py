# Define the function to calculate light-year
def light_year():
    """
    Returns the distance (in meters) represented by one light-year.
    """

    # Return the light-year as per the formula "one light-year = 31536000 * 3 * 10^8 meters"
    return 31536000 * 3 * 10**8